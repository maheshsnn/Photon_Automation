package Practice;

public class Matrix_Transpose {
	
	
	public static void main(String[] args) {
		
		
		
		int original[][]={{1,3,4},{2,4,3},{3,4,5}};    
		
		for(int i=0;i<original.length;i++)
		{
			for(int j=0;j<original.length;j++)
			{
				
				
					
					System.out.print(original[j][i]);
					
				
			}
			System.out.println();
		}
		
		
	}

}
